<script>
    let track = document.getElementById('successTrack');
    let scrollAmount = 0;

    function autoSlide() {
        scrollAmount += 270;

        if (scrollAmount >= track.scrollWidth - track.clientWidth) {
            scrollAmount = 0;
        }

        track.style.transform = `translateX(-${scrollAmount}px)`;
    }

    setInterval(autoSlide, 3000);
</script>